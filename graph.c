// a graph consists of a set of vertices and a set of edges(arcs) connecting
// the vertices. The graph is represented by an adjacency matrix.
// G=(V,E) where V is a finite and not empty set of vertices and E is a set of pairs of vertices, their pairs are called edges.
// Therefore, V(G) is the set of vertices and E(G) is the set of edges.
// e=(V,W) is an edge of G if and only if V,W are vertices of G. and said to be incendent to V and W.

// Undirected Graph
// In an undirected graph a pair of vertices representing any edge is unordered thus, (V,W) = (W,V).

// Directed Graph
// In a directed graph a pair of vertices representing any edge is ordered thus, (V,W) != (W,V).
// If e=(V,W) then V is the initial vertex and W is the terminal vertex. Thus subsequently (V,W) and (W,V) are two different edges.
// Vertex v1 is said to be adjacent to the vertex v2 if there is a directed edge from v1 to v2 or from v2 to v1.
// Path: A path is a sequence of vertices in which from each of its vertices there is an edge to the next vertex in the sequence.